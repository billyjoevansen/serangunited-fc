export const AVATAR_SERVICES = {
  dicebear: (seed, style = "avataaars") => 
    `https://api.dicebear.com/7.x/${style}/svg?seed=${encodeURIComponent(seed)}`,
  uiAvatars: (name) => 
    `https://ui-avatars.com/api/?name=${encodeURIComponent(name)}&background=random&size=200`,
  robohash: (seed) => 
    `https://robohash.org/${encodeURIComponent(seed)}?set=set4&size=200x200`,
  pravatar: (id) => 
    `https://i.pravatar.cc/200?u=${encodeURIComponent(id)}`,
  randomUser: (gender, id) =>
    `https://randomuser.me/api/portraits/${gender === 'men' ? 'men' : 'women'}/${id % 100}.jpg`
}


